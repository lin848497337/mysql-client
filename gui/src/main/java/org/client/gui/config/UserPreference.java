package org.client.gui.config;

public class UserPreference {

    private int x;
    private int y;
    private int width;
    private int   height;
    private int honSplitLoc;
    private int vertSplitLoc;

    public int getX() {
        return x;
    }

    public void setX(int x) {
        this.x = x;
    }

    public int getY() {
        return y;
    }

    public void setY(int y) {
        this.y = y;
    }

    public int getWidth() {
        return width;
    }

    public void setWidth(int width) {
        this.width = width;
    }

    public int getHeight() {
        return height;
    }

    public void setHeight(int height) {
        this.height = height;
    }

    public int getHonSplitLoc() {
        return honSplitLoc;
    }

    public void setHonSplitLoc(int honSplitLoc) {
        this.honSplitLoc = honSplitLoc;
    }

    public int getVertSplitLoc() {
        return vertSplitLoc;
    }

    public void setVertSplitLoc(int vertSplitLoc) {
        this.vertSplitLoc = vertSplitLoc;
    }
}
